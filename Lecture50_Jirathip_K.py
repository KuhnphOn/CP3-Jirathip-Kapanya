def add_number(x, y):
    print("x+y =", x+y)


def minus_number(x, y):
    print("x-y =", x-y)


def multiply_number(x, y):
    print("x*y =", x*y)


def divide_number(x, y):
    print("x/y =", x/y)


def combination(x, y):
    add_number(x, y)
    minus_number(x, y)
    multiply_number(x, y)
    divide_number(x, y)


combination(5, 10)
